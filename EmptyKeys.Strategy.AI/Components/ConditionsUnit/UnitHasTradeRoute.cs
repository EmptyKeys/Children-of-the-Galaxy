using EmptyKeys.Strategy.Trade;

namespace EmptyKeys.Strategy.AI.Components.ConditionsUnit
{
    /// <summary>
    /// Implements unit condition for behavior.
    /// </summary>
    /// <seealso cref="EmptyKeys.Strategy.AI.Components.BehaviorComponentBase" />
    public class UnitHasTradeRoute : BehaviorComponentBase
    {
        /// <summary>
        /// Initializes a new instance of the <see cref="UnitHasTradeRoute"/> class.
        /// </summary>
        public UnitHasTradeRoute()
            : base()
        {
        }

        /// <summary>
        /// Executes behavior with given context
        /// </summary>
        /// <param name="context">The context.</param>
        /// <returns></returns>
        public override BehaviorReturnCode Behave(IBehaviorContext context)
        {
            UnitBehaviorContext unitContext = context as UnitBehaviorContext;
            if (unitContext == null)
            {
                returnCode = BehaviorReturnCode.Failure;
                return returnCode;
            }

            ITradeUnit tradeUnit = unitContext.Unit as ITradeUnit;        
            if (tradeUnit != null && tradeUnit.HasAssignedTradeRoute)
            {
                returnCode = BehaviorReturnCode.Success;
                return returnCode;
            }

            returnCode = BehaviorReturnCode.Failure;
            return returnCode;
        }
    }
}
